    <p>Every spring semester, Ramblin’ Reck Club holds RECKruitment in order to take in a new class of probates (probationary members). These probates are selected from a pool of applicants who must first submit an application and attend a series of RECKruitment events. This year's RECKruitment cycle will be held in-person with appropriate COVID-19 precautions (mask wearing, social distancing, and testing) being observed. Any issues related to COVID-19 quarantine/isolation will be worked out on a case-by-case basis. Email <a href="mailto:membershipchair@reckclub.org">Derek Prusener and Abi Ivemeyer</a>, Ramblin' Reck Club Membership Co-Chairs, if you have questions or concerns. Find the breakdown of these events below:</p>

    <h4 class="mb-3">Overview of the 2022 Application Process and Timeline</h4>

    <p class="mb-0">A full and complete application for membership includes:</p>
<!--    <p class="mt-0 text-muted"><i>Note: submission deadline and all event dates TBD for 2020</i></p>-->
    <ul>
        <li>A <a href="#written-application">written application</a> submitted by <strong>Friday, February 4th</strong></li>
        <li>Attendance at one of the three selected <a href="#gtky-events">“Get to Know You” (GTKY)</a> Events</li>
        <li>Attendance at the <a href="#first-social">first social</a> on <strong>Monday, February 14th</strong></li>
        <li><a href="#interviews">Interviews</a> (individual and group), <strong>February 15th - February 17th</strong></li>
        <li>Attendance at the <a href="#second-social">second social</a> on <strong>February 20th</strong> (by invitation only)</li>
        <li>Attendance at the all-club Spring Retreat (if selected to be a 2022 probate)</li>
    </ul>

    <p>For details about each of the above requirements, check out the sections below or click on the requirement for more information!</p>

    <p>Any absences or potential conflicts with the required events should be brought to the attention of <a href="mailto:membershipchair@reckclub.org">Derek Prusener and Abi Ivemeyer</a>, Ramblin’ Reck Club Membership Co-Chairs, no later than Friday, February 4th.</p>

<h4 class="mb-2">Probate Requirements</h4>

<p>Before applying be sure to read the following requirements:</p>

<ul>
    <li>Applicants may <strong>not</strong> graduate prior to December 2022.</li>
    <li>Applicants must be able to attend a weekly probate meeting on Monday evenings from 6-7 PM and then the full club meeting at 7 PM. Exceptions to this can be made, but very rarely.</li>
    <li>Spring 2022 out-of-town co-ops and study abroad students are not eligible to apply this RECKruitment season. Fall 2022 out-of-town co-ops and study abroad students will be dealt with on a case by case basis. Email <a href="mailto:membershipchair@reckclub.org">Derek Prusener and Abi Ivemeyer</a>, Ramblin’ Reck Club Membership Co-Chairs, if you have questions or concerns.</li>
</ul>

<h4 class="mb-2">Tips for RECKruitment</h4>

<ul>
    <li>Be yourself! We want to get to know you and why you love Georgia Tech!</li>
    <li>Have fun! The socials and "Get To Know You" events will have fun activities and should not feel like an interview; that's what the interview is for.</li>
    <li>Meet as many members as possible across the socials and "Get To Know You" events. Don't be afraid to ask a member you already know to introduce you to someone new.</li>
    <li>Put time and effort into your application. Follow the suggested word counts for each question to make sure you answer it fully.</li>
    <li>While dress codes are listed for both the socials and interview, we understand there may be circumstances that may prevent some applicants from having proper attire. Don't sweat it, we just want to see you there regardless! If you do need assistance with interview clothing, <a href="https://career.gatech.edu/campus-closet-open">Georgia Tech's Campus Closet</a> provides this service free of charge to students.</li>
</ul>

