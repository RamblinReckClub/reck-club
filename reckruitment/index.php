    <p>In order to provide applicants a casual setting to interact with Reck Clubbers and get a better feel for the kind of spirit Reck Club has for Georgia Tech, all applicants must attend one of the following GT Athletic events. Please note that applicants need only attend one of the following athletic events in order to satisfy the recruitment requirements:</p>

<ul>
    <li><strong>Thursday, January 23rd</strong>: Women’s Basketball game vs. UNC at 6pm in McCamish Pavilion</li>
    <li><strong>Wednesday, January 29th</strong>: Tech Rec Bowling Social from 7:30pm-9:30pm in the Student Center's Tech Rec</li>
    <li><strong>Saturday, February 1st</strong>: Men’s Basketball game watch party vs. Notre Dame at 12:00pm (Location TBD)</li>
