<h2 class="mb-3">RECKruitment Process</h2>
    <img class="img-responsive mb-2" src="/img/promo/old-rrc-polo.jpg" width="100%">
    <p>Every spring semester, Ramblin’ Reck Club holds RECKruitment in order to take in a new class of probates (probationary members). These probates are selected from a pool of applicants who must first submit an application and attend a series of RECKruitment events. Find the breakdown of these events below:</p>

    <h4 class="mb-3">Overview of Application Process and Timeline</h4>

    <p class="mb-0">A full and complete application for membership includes:</p>
<!--    <p class="mt-0 text-muted"><i>Note: submission deadline and all event dates TBD for 2020</i></p>-->
    <ul>
        <li>A <a href="#written-application">written application</a> submitted by <strong>January 31</strong></li>
        <li>Attendance at one of the three selected <a href="#gtky-events">“Get to Know You” (GTKY)</a> Events</li>
        <li>Attendance at the <a href="#first-social">first social</a> on <strong>Monday, February 3rd</strong></li>
        <li><a href="#interviews">Interviews</a> (individual and group), <strong>February 4th - February 6th</strong></li>
        <li>Attendance at the <a href="#second-social">second social</a> on <strong>February 9th</strong> (by invitation only)</li>
        <li>Attendance at the all-club Spring Retreat (if selected to be a 2020 probate)</li>
    </ul>

    <p>For details about each of the above requirements, check out the sections below or click on the requirement for more information!</p>

    <p>Any absences or potential conflicts with the required events should be brought to the attention of <a href="mailto:membershipchair@reckclub.org">Kassie Lee</a>, Ramblin’ Reck Club Recruitment Chair, no later than Saturday, February 1st.</p>

    <br>
<h4 class="mb-3">Probate Requirements</h4>

<p>Before applying be sure to read the following requirements:</p>

<ul>
    <li>Applicants must be in good academic and judicial standing. We will verify your academic and judicial standing with the Institute.</li>
    <li>Applicants may <strong>not</strong> graduate prior to December 2020.</li>
    <li>Applicants must be able to attend a weekly probate meeting on Monday evenings from 6:15-7:15 PM and then the full club meeting at 7:15 PM. Exceptions to this can be made, but very rarely.</li>
    <li>Spring 2020 out-of-town co-ops and study abroad students are not eligible to apply this RECKruitment season. Fall 2020 out-of-town co-ops will be dealt with on a case by case basis. Email <a href="mailto:membershipchair@reckclub.org">Kassie Lee</a>, Ramblin’ Reck Club Recruitment Chair, if you have questions or concerns.</li>
</ul>

