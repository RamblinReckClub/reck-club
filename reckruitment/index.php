<h4 class="mb-3" id="interviews">Interviews (Mandatory)</h4>

    <p>The interview portion of RECKruitment is designed to allow for Reck Clubbers to get to know applicants on a more personal basis. No need to stress! We just want to give each applicant the time to express their ideas, commitment, and personality. The interview stage will include a 20 minute individual interview followed by a short group interview activity. Dress code for this event is <strong>business semi-formal</strong>, and will be hosted in the Student Success Center Skyboxes. <a href="http://admission.gatech.edu/visit/bill-moore-student-success-center">Click here</a> for directions to the building, and be on the lookout for directions inside the building to the Skyboxes once you arrive. Interviewees will sign up for an interview time slot during check-in at the first social.</p>
    <ul>
        <li><strong>Tuesday, February 4th through Thursday, February 6th from 7:00pm-10:00pm</strong></li>
    </ul>

    <br>
<h4 class="mb-3" id="second-social">Second Social (Mandatory)</h4>
    <p>After all interviews have been conducted, the Recruitment Chair will notify each applicant whether they will advance to the second round of RECKruitment. During this second round, applicants will attend a mandatory second social (invitation only) held on the <strong>afternoon of Sunday, February 9th</strong>. Further details for this event will be published to applicants personally.</p>

    <br>
    <p>Any time conflicts with these events should be brought to the attention of <a href="mailto:membershipchair@reckclub.org">Kassie Lee</a>, Ramblin’ Reck Club Recruitment Chair, no later than <strong>Saturday, February 1st</strong>.</p>


