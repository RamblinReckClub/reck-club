<h4 class="mb-2" id="first-social">First Social (Mandatory)</h4>
    <p>Attending this social is your chance as an applicant to meet Reck Clubbers, get a feel for what Ramblin’ Reck Club is all about, ask any questions you may have about the Club or getting involved, and most importantly, sign up for an interview time. No need to stress about this event; be yourself and talk to anyone you do not already know! Dress code for this event is <strong>business casual</strong>, and will be hosted in-person at ???.</p>
    <ul>
        <li><strong>First Social: Monday, February 14th</strong></li>
    </ul>
    <p>There are two time slots for the first social: <strong>7:00pm-8:00pm and 8:30pm-9:30pm</strong>. Please note down your availability on the application. If you are not available during either of these times, please email <a href="mailto:membershipchair@reckclub.org">Derek Prusener and/or Abi Ivemeyer</a>.</p>

<h4 class="mb-2" id="interviews">Interviews (Mandatory)</h4>

    <p>The interview portion of RECKruitment is designed to allow for Reck Clubbers to get to know applicants on a more personal basis. We just want to give each applicant the time to express their ideas, commitment, and personality. The interview portion will include an individual interview followed by a short group interview activity. The encouraged attire is business semi-formal, but wear what is comfortable. Interviewees will sign up for an interview time slot during check-in at the first social.</p>
    <ul>
        <li><strong>Tuesday, February 2nd from 7:00pm-9:00pm</strong></li>
        <li><strong>Wednesday, February 3rd from 7:00pm-9:00pm</strong></li>
        <li><strong>Thursday, February 4th from 7:00pm-9:00pm</strong></li>
    </ul>

<h4 class="mb-2" id="second-social">Second Social (Mandatory)</h4>
    <p>After all interviews have been conducted, the Membership Chairs will notify each applicant whether they will advance to the second round of RECKruitment. During this second round, applicants will attend a mandatory second social held from <strong>2:00pm-4:00pm on Sunday, February 20th</strong>. Further details for this event will be sent out to those invited applicants only.</p>

