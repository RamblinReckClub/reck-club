<h4 class="mb-2">Probate Requirements</h4>

<p>Before applying be sure to read the following requirements:</p>

<ul>
    <li>Applicants must be in good academic and judicial standing. We will verify your academic and judicial standing with the Institute.</li>
    <li>Applicants may <strong>not</strong> graduate prior to December 2021.</li>
    <li>Applicants must be able to attend a weekly probate meeting on Monday evenings from 6-7 PM and then the full club meeting at 7 PM. Exceptions to this can be made, but very rarely.</li>
    <li>Spring 2021 out-of-town co-ops and study abroad students are not eligible to apply this RECKruitment season. Fall 2021 out-of-town co-ops will be dealt with on a case by case basis. Email <a href="mailto:membershipchair@reckclub.org">Whitney Miller</a>, Ramblin’ Reck Club Recruitment Chair, if you have questions or concerns.</li>
</ul>

