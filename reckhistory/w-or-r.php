    <h2 id="the-controversy">The Controversy</h2>
<p>The name of Georgia Tech’s automobile mascot is a point of fierce contention. The problem comes from varied use of the word “Wreck” and its contraction “Reck” when referring to the modern-day 1930 Ford Model A sport coupe or even the mechanical contraptions built by Georgia Tech students for homecoming parades before the school’s acquisition of the official mascot car in 1961.</p>

<p>The earliest official reference to the mascot in the Georgia Tech archives is <a href="https://smartech.gatech.edu/handle/1853/31502">the 1962 Blueprint</a>, in which the writers of the Athletics section refer to the car as the “Ramblin’ Reck”. However, references to <a href="https://smartech.gatech.edu/handle/1853/25251">Dean Floyd Field’s car</a>, the <a href="https://smartech.gatech.edu/handle/1853/26077">Georgia Tech football team</a>, and the <a href="https://smartech.gatech.edu/handle/1853/26066">Tech student body</a> in the Technique that predate the mascot use both spellings interchangeably.</p>

<p>The historic use of both terms has created a lot of confusion amongst students and fans, and the Institute has further complicated matters by holding a <a href="http://www.licensing.gatech.edu/trademarks-101">trademark on “Ramblin’ Wreck”</a> and discouraging the use of “Ramblin’ Reck”. But the question remains: which name is accurate? Well, it depends on who you ask.</p>

<p><strong>The Reck Club stance</strong>: “Wreck” refers to students; “Reck” refers to the car and the club.</p>

<p><strong>The Institute stance</strong>: “Wreck” refers to both the car and students; “Reck” is not used.</p>

